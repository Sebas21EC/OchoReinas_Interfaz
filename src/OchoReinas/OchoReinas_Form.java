    private void btnVerificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVerificarActionPerformed
        int[] arreglo_tablero = new int[8];
        int a = 0;
        for (int i = 7; i >= 0; i--) {
            for (int j = 0; j < 8; j++) {
                if (reinascolocadas[i][j]) {
                    arreglo_tablero[a] = j;
                    break;
                }
            }
            a++;
        }
        boolean exito = true;
        for (int i = 0; i < 8; i++) {
            if (!OchoReinas.comprobar(arreglo_tablero, i)) {
                exito = false;
                break;
            }

        }

        if (exito) {
            JOptionPane.showMessageDialog(null, "Buen trabajo");
        } else {
            JOptionPane.showMessageDialog(null, "Siga intentando");
        }

    }//GEN-LAST:event_btnVerificarActionPerformed

