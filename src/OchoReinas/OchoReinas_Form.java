    /**
     * Metodo que agrega imagen de reina negra en boton
     *
     * @param boton Nombre del boton
     * @param fila Fila de boton
     * @param columna Columan del Boton
     */
    private void IconoReinaNegra(JButton boton, int fila, int columna) {
        if (n_reinas < 8) {
            if (!reinascolocadas[fila][columna]) {
                boton.setIcon(icono_negro);
                reinascolocadas[fila][columna] = true;
                n_reinas++;
            } else {
                boton.setIcon(null);
                reinascolocadas[fila][columna] = false;
                n_reinas--;
            }
        } else {
            if (!reinascolocadas[fila][columna]) {
                JOptionPane.showMessageDialog(null, "Ya se han ingresado las 8 reinas");
            } else {
                boton.setIcon(null);
                reinascolocadas[fila][columna] = false;
                n_reinas--;
            }
        }

    }

