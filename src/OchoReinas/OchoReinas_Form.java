    private void AgregarDatos() {
        
        modelo = (DefaultListModel) lstSoluciones.getModel();
        
        for (int i = 0; i < ocho_reinas.getArreglo_soluciones().length; i++) {
            modelo.addElement((i+1)+".-  "+ocho_reinas.getArreglo_soluciones()[i]); 
        }

    }

    private void limpiarJlist() {
        modelo = (DefaultListModel) lstSoluciones.getModel();
        modelo.clear();
    }

